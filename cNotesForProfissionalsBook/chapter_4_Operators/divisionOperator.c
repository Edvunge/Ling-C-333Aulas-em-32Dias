#include <stdio.h>

int main(void) {
    // division Operator
    int a = 19 / 2;
    int b = 18 / 2;
    int c = 255 / 2;
    int d = 44 / 2;
    double e = 19 / 2.0;
    double f = 18.0 / 2;
    double g = 255 / 2.0;
    double h = 45.0 / 4;

    printf("19 / 2 = %d\n",a);
    printf("18 / 2 = %d\n",b);
    printf("255 / 2 = %d\n",c);
    printf("44 / 4 = %d\n",d);
    printf("19 / 2.0 = %g\n",e);
    printf("18.0 / 2 = %g\n",f);
    printf("255 / 2.0 = %g\n",g);
    printf("45.0 / 4 = %g\n",h);
    return (0);
}