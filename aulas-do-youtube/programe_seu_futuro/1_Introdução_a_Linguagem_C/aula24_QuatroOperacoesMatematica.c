#include <stdio.h>
#include <stdlib.h>
/*
	Aula 24
	Operadores Matematicos:
	Soma   				+ 
	Subtracção 			-
	Multiplicação 		*
	Divisão 			/
*/

int main(void) {
	printf("\nSoma: %d\n", 10 + 20);
	printf("\nSubtracao: %d\n", 10 - 20);
	printf("\nMultiplicacao: %d\n", 10 * 20);
	printf("\nDivisao: %f\n", 10.0 / 20);
	return 0;
}
