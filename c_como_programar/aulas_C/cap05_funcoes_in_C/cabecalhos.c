#include <stdio.h>


#include <assert.h> //Contém macros e informações que acrescentam diagnósticos 
                    // que auxiliam a depuração do programa.

#include <ctype.h> // Contém protótipos de função para funções que testam certas propriedades 
                   // dos caracteres, 
                   // e protótipos de função para funções que podem ser 
                   // usadas para converter letras minúsculas em maiúsculas, e vice-versa.


#include <errno.h> // Define macros que são úteis na comunicação de condições de erro.

#include <float.h> // Contém os limites de tamanho de ponto flutuante do sistema.

#include <limits.h> // Contém os limites de tamanho de inteiros do sistema.

#include <locale.h> // Contém protótipos de função e outras informações que permitem 
                    // que um programa seja modificado para
                    // o local em que estiver sendo executado. 
                    // A noção de local permite que o sistema de computação trate de
                    // diferentes convenções que expressam dados como datas, horas, 
                    // valores monetários e números grandes em qualquer lugar do mundo.


#include <math.h> // Contém protótipos de função para funções da biblioteca matemática.

#include <setjmp.h> // Contém protótipos de função para funções que permitem evitar a 
                    // sequência normal de chamada e de retorno de função.


#include <signal.h> // Contém protótipos de função e macros que lidam com diversas 
                    // condições que podem surgir durante a execução do programa.

#include <stdarg.h> // Define macros que lidam com uma lista de argumentos para uma função cujo número 
                    // e cujo tipo são desconhecidos.

#include <stddef.h> // Contém as definições comuns de tipo usadas pela C para realizar cálculos.

#include <stdio.h> // Contém protótipos de função para as funções da biblioteca-padrão de entrada/saída, 
                   // e informações usadas por eles.

#include <stdlib.h> // Contém protótipos de função para conversões de números em texto e 
                    // de texto em números,
                    // alocação de memória, números aleatórios e outras funções utilitárias.

#include <string.h> // Contém protótipos de função para funções de processamento de strings.

#include <time.h>   // Contém protótipos de função e tipos para manipulação de hora e data.

